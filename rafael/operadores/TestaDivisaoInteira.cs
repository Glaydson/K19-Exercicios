class TestaDivisaoInteira 
{
	static void Main() 
	{
		int a = 5;
		int b = 2;
		
		System.Console.WriteLine(a / b);
		System.Console.WriteLine((double)a / b);
		System.Console.WriteLine(a / (double)b);
		System.Console.WriteLine((double)a / (double)b);
		System.Console.WriteLine((double)(a / 2));
	}
}
