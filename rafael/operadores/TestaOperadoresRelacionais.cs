class TestaOperadoresRelacionais 
{
	static void Main() 
	{
		int a = 1;
		int b = 2;
		
		System.Console.WriteLine(a > b);
		System.Console.WriteLine(a >= b);
		System.Console.WriteLine(a < b);
		System.Console.WriteLine(a <= b);
		System.Console.WriteLine(a == b);
		System.Console.WriteLine(a != b);
	}
}
